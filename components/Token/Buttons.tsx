import useConnectedWallet from "@/hooks/useConnectedWallet";
import useDownload from "@/hooks/useDownload";
import useShareMoment from "@/hooks/useShareMoment";
import { useTokenProvider } from "@/providers/TokenProvider";
import Image from "next/image";
import { useRouter } from "next/navigation";

const Buttons = () => {
  const { setCollected } = useTokenProvider();
  const { push } = useRouter();
  const { connectedWallet } = useConnectedWallet();
  const { share } = useShareMoment();
  const { download, isDownloading } = useDownload();

  const visit = () => {
    setCollected(true);
    push(`/${connectedWallet}`);
  };

  return (
    <div className="space-y-2 pt-3 relative">
      <div className="absolute w-1/2 aspect-[1/1] -right-10 bottom-0">
        <Image
          src="/semi-transparent.png"
          alt="not found semi"
          layout="fill"
          objectFit="cover"
          objectPosition="center"
        />
      </div>
      <button
        type="button"
        className="w-full py-2 bg-black font-archivo text-grey-eggshell rounded-sm relative text-2xl"
        onClick={share}
      >
        share
      </button>
      <button
        type="button"
        className="w-full py-2 font-archivo text-black border border-black rounded-sm relative text-2xl"
        onClick={visit}
      >
        visit timeline
      </button>
      <button
        type="button"
        className="w-full py-2 font-archivo text-black border border-black rounded-sm relative text-2xl"
        onClick={download}
        disabled={isDownloading}
      >
        download
      </button>
    </div>
  );
};

export default Buttons;
